const add = require('./hw5');

describe('hw5', () => {
  it('should return correct answer when a=111111111111111111111111111111111111 and b=111111111111111111111111111111111111', () => {
    expect(add('111111111111111111111111111111111111', '111111111111111111111111111111111111')).toBe('222222222222222222222222222222222222');
  });
  it('should return correct answer when a=123 and b=456', () => {
    expect(add('123', '456')).toBe('579');
  });
  it('should return correct answer when a=12312383813881381381 and b=129018313819319831', () => {
    expect(add('12312383813881381381', '129018313819319831')).toBe('12441402127700701212');
  });
  it('should return correct answer when a=9999999999999999999999 and b=9999999999999999999999', () => {
    expect(add('9999999999999999999999', '9999999999999999999999')).toBe('19999999999999999999998');
  });
  it('should return correct answer when a=555555555555555555555555555555 and b=555555555555555555555555555555', () => {
    expect(add('555555555555555555555555555555', '555555555555555555555555555555')).toBe('1111111111111111111111111111110');
  });
  it('should return correct answer when a=0 and b=0', () => {
    expect(add('0', '0')).toBe('0');
  });
  it('should return correct answer when a=9999999999999999999999999 and b=0', () => {
    expect(add('9999999999999999999999999', '0')).toBe('9999999999999999999999999');
  });
  it('should return correct answer when a=9999999999999999999999999 and b=1', () => {
    expect(add('9999999999999999999999999', '1')).toBe('10000000000000000000000000');
  });
});
